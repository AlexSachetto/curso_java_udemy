package Controle;

public class Whiledeterminado {
public static void main(String[] args) {
	int contador = 1;
	while(contador <= 10) { // usa WHILHE para repetição o ( <= 10 "para comandar quantas linhas voce quer")
		System.out.printf("i = %d\n", contador);
		contador++; // vai precissar para nao ficar contando infinitas vezes "Assim ele vai acrecentando até chegar no 10.
		
		
	}
}
}
